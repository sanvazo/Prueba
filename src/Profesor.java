
public class Profesor {
	
	private String nombre;
	private String dni;
	private int telefono;
	
	
	/**
	 * @param nombre
	 * @param dni
	 * @param telefono
	 */
	public Profesor(String nombre, String dni, int telefono) {
		super();
		this.nombre = nombre;
		this.dni = dni;
		this.telefono = telefono;
	}
	
	

}
